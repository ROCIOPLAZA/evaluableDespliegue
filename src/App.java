public class App {

    public static void bucle() {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println(
                "Acabo de realizar un bucle del 1 al 10 para que salga cada número una debajo del otro. Y esto añadiendo este código para el cambio del reto");
    }

    public static void main(String[] args) throws Exception {
        System.out.println("Hola esto es un código de prueba para el evaluable!!");

        bucle();

    }
}
